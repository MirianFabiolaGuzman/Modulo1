// Programa que imprime mediante un ciclo los numeros pares del 2 a 100
public class Ejercicio_7 
{
    public static void main(String[] args) 
    {
        for (int i = 2 ; i <= 100 ; i += 2)
        {
            System.out.println(i);
        }
    }
}